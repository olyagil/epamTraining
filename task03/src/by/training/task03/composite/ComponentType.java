package by.training.task03.composite;

/**
 * In the {@code ComponentType} enum is listed the types of the object.
 */
public enum ComponentType {
    /**
     * Text type of the component.
     */
    TEXT,
    /**
     * Paragraph type of the component.
     */
    PARAGRAPH,
    /**
     * Sentence type of the component.
     */
    SENTENCE,
    /**
     * Lexeme type of the component.
     */
    LEXEME,
    /**
     * Word type of the component.
     */
    WORD,
    /**
     * Expression type of the component.
     */
    EXPRESSION,
    /**
     * Punctuation mark type of the component.
     */
    PUNCTUATION_MARK,
    /**
     * Symbol type of the component.
     */
    SYMBOL
}
